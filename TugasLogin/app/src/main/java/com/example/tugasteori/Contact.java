package com.example.tugasteori;

public class Contact {
    public String name;
    public String phone;
    public String email;
    public String avatarName;

    public Contact(String name, String phone, String email, String avatarName) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.avatarName = avatarName;
    }
}
